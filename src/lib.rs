//! r-wg: 原生 WireGuard 桌面客户端
//!
//! 本项目是一个跨平台（Linux/Windows）的 WireGuard VPN 客户端，
//! 采用 Rust 原生实现，具有以下特点：
//!
//! # 核心特性
//! - **GPUI 界面**: 使用 Zed.dev 的 GPUI 框架构建原生 UI
//! - **特权分离**: UI 运行于普通权限，特权操作（网络配置）由独立后端服务处理
//! - **WireGuard 引擎**: 基于 gotatun 库实现隧道管理
//! - **路由可视化**: 提供路由规划图（Route Map）展示流量走向
//!
//! # 架构设计
//! ```text
//! ┌─────────────────────────────────────────────────────┐
//! │                    UI 进程 (GPUI)                    │
//! │  ┌─────────┐  ┌─────────┐  ┌─────────────────────┐  │
//! │  │ 视图层  │  │ 状态层  │  │   功能控制器        │  │
//! │  └─────────┘  └─────────┘  └─────────────────────┘  │
//! └──────────────────────┬──────────────────────────────┘
//!                        │ IPC (Unix Domain Socket / Windows Pipe)
//! ┌──────────────────────┴──────────────────────────────┐
//! │              特权后端服务 (root/admin)               │
//! │  ┌─────────────┐  ┌─────────────┐  ┌───────────┐  │
//! │  │ WireGuard   │  │  网络配置   │  │  路由规划  │  │
//! │  │   引擎      │  │  (netlink) │  │           │  │
//! │  └─────────────┘  └─────────────┘  └───────────┘  │
//! └─────────────────────────────────────────────────────┘
//! ```
//!
//! # 模块说明
//! - [`application`]: 应用级服务（隧道会话、配置库、诊断服务）
//! - [`backend`]: WireGuard 后端引擎与特权服务
//! - [`core`]: 核心共享逻辑（配置解析、路由规划）
//! - [`dns`]: DNS 配置与管理
//! - [`log`]: 日志系统（基于 tracing）
//! - [`platform`]: 平台特定实现（Linux/Windows 网络操作）

#![deny(clippy::print_stdout, clippy::print_stderr)]

/// 应用级服务模块
///
/// 包含隧道会话管理、配置库、诊断服务等高层抽象。
/// 这些服务协调 UI 层与后端引擎的交互。
pub mod application;

/// WireGuard 后端引擎模块
///
/// 包含核心引擎、特权服务、IPC 协议实现。
/// - Linux: systemd 服务模式
/// - Windows: SCM 服务模式
pub mod backend;

/// 核心共享逻辑模块
///
/// 不依赖平台或 UI 的核心功能：
/// - WireGuard 配置解析
/// - 路由规划算法
pub mod core;

/// DNS 配置与管理模块
pub mod dns;

/// 日志系统模块
///
/// 基于 `tracing` 框架，支持结构化日志和日志级别过滤。
pub mod log;

/// TLS 初始化模块
pub mod tls;

/// 平台特定实现模块
///
/// 针对不同操作系统的网络配置差异：
/// - [`platform::linux`]: Linux netlink 网络操作
/// - [`platform::windows`]: Windows 网络API操作
pub mod platform;
